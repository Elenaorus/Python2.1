n=int(input("Введите число: "))
while n<0 or n>100000:
    if n<0:
        print('Вы ввели отрицательное число: ')
    elif n>100000:
        print("Вы ввели число больше 100000")
    n=int(input("Введите число: "))

k=0
for i in range(1,n+1):
    if n%i==0:
            k+=1
if k<=2:
        print('Число является простым')
else:
        print('Число является составным')
